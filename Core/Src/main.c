float read_temp(void){
	uint16_t temp = 0;
	uint8_t temp1 = 0;
	uint8_t temp2 = 0;

	if (initialize_comm() == HAL_OK) {
		write_byte(0xCC);
		write_byte(0x44);
		HAL_Delay(760);
	}

	if (initialize_comm() == HAL_OK) {
			write_byte(0xCC);
			write_byte(0xBE);
			temp1 = read_byte();
			temp2 = read_byte();
			temp = temp2;
			temp <<= 8;
			temp |= temp1;
		}

	float temp_f = 0;
	for (int i=0; i<11; i++){
		uint8_t val = 0x01 & (temp >> i);
		temp_f += (float) val * pow(2, i-4);
	}

	if (0x01 & (temp >> 14)) temp_f *= -1;

	return temp_f;
}


uint8_t ROM_CRC = 0;
uint8_t update_CRC(uint8_t actual_crc, uint8_t input_byte){
	uint8_t new_crc = 0;
	uint8_t input_bit = 0;
	uint8_t mask = 1;
	// Dla każdego bitu z bajtu wejściowego
	for (uint8_t i = 0; i < 8; i++){
		input_bit = (((input_byte >> i) & 1) ^ (actual_crc & 1)); // XOR na wejściu
		// Aktualizuj shift register
		for(uint8_t j = 0; j < 8; j++){
			mask = 1;
			mask <<= j;
			new_crc &= ~mask;  // Wyzeruj aktualnie rozważany bit
			// Funcje ustawiające shift register na podstawie datasheetu
			if (j == 2 || j == 3)
				new_crc |= ((actual_crc & (mask << 1)) ^ (input_bit << (j+1))) >> 1;
			else if(j == 7)
				new_crc |= input_bit << 7;
			else
				new_crc |= (actual_crc & (mask << 1)) >> 1;
		}
		actual_crc = new_crc;
	}
	return new_crc;
}
